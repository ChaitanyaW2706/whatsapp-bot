def format_mobile(phone: str) -> str:
    """
    Format an Indian mobile number to the +91-XXXXXXXXXX display form.

    Accepted input formats:
        918688977412  →  +91-8688977412
        8688977412    →  +91-8688977412
        Any other     →  returned as-is (safe fallback)
    """
    phone = phone.strip()
    if phone.startswith("91") and len(phone) == 12:
        return f"+91-{phone[2:]}"
    if len(phone) == 10:
        return f"+91-{phone}"
    return phone


def normalize_text(text: str) -> str:
    """Normalize user text for robust keyword matching."""
    t = (text or "").lower()
    corrections = {
        "renwal": "renew",
        "renwe": "renew",
        "renw": "renew",
        "estimtae": "estimate",
        "estmate": "estimate",
        "estimat": "estimate",
        "estimte": "estimate",
        "quate": "quote",
        "quoute": "quote",
        "polciy": "policy",
        "premuim": "premium",
        "prmium": "premium",
    }
    for wrong, right in corrections.items():
        t = t.replace(wrong, right)
    return t.strip()


