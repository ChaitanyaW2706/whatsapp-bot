def is_genuine_query(text: str, state: str = "") -> bool:
    """
    Returns True ONLY if the user's text is a genuine question or query
    that should be answered by the AI assistant.

    Returns False for:
      - Vehicle registration numbers (e.g. KA01AB1234)
      - Short inputs (1–2 words) that look like form inputs
      - Pure numbers or alphanumeric codes
      - States that always expect structured form inputs
      - Greetings that should restart the flow, not trigger AI

    Args:
        text  : The raw user message text.
        state : The current flow state (used to block AI in form states).

    Returns:
        bool — True = let AI respond, False = flow handles it.
    """
    if not text:
        return False

    t = text.strip()
    t_lower = t.lower()
    words = t_lower.split()
    word_count = len(words)

    # ── 1. Block AI entirely in structured-input states ─────────────────
    if state in _STRUCTURED_INPUT_STATES or any(str(state).startswith(prefix) for prefix in _STRUCTURED_INPUT_STATE_PREFIXES):
        print(f"[query_gate] BLOCK — structured input state: {state}")
        return False

    # ── 1b. Explicit Intent Check ───────────────────────────────────────
    # If the text matches a known insurance intent (Renew, Estimate, etc.),
    # allow it through so the AI router can execute the flow switch.
    if classify_insurance_intent_request(text):
        print(f"[query_gate] ALLOW — explicit insurance intent: '{t}'")
        return True

    # ── 2. Vehicle registration number pattern ───────────────────────────
    #    Format: 2 letters + 2 digits + optional 2 letters + 4 digits
    #    e.g. KA01AB1234, KL21W2285, MH12DE1433
    if re.match(r'^[A-Za-z]{2}\d{2}[A-Za-z]{0,3}\d{4}$', t.replace(' ', '')):
        print(f"[query_gate] BLOCK — looks like vehicle reg: '{t}'")
        return False

    # ── 3. Pure number or very short code ───────────────────────────────
    if re.match(r'^\d+$', t):
        print(f"[query_gate] BLOCK — pure number: '{t}'")
        return False

    # ── 4. Single word that's just an alphanumeric code ─────────────────
    if word_count == 1 and re.match(r'^[A-Za-z0-9_]{1,20}$', t):
        # Single short words could be menu IDs, codes, names.
        # Exception: if it's a known question word, let it through.
        if t_lower not in _QUESTION_WORDS:
            print(f"[query_gate] BLOCK — single non-question word: '{t}'")
            return False

    # ── 5. Too short to be a real question (1–2 words, no question mark) ─
    if word_count < _MIN_QUERY_WORDS and '?' not in t:
        # Allow known question words even if short (e.g. "how?", "why?")
        if not any(w in _QUESTION_WORDS for w in words):
            print(f"[query_gate] BLOCK — too short, no question mark: '{t}'")
            return False

    # ── 6. Explicit question mark → definitely a query ──────────────────
    if '?' in t:
        print(f"[query_gate] ALLOW — has question mark: '{t}'")
        return True

    # ── 7. Starts with or contains a question word ───────────────────────
    if words and words[0] in _QUESTION_WORDS:
        print(f"[query_gate] ALLOW — starts with question word: '{t}'")
        return True

    if any(qw in t_lower for qw in ["tell me", "explain", "describe", "clarify",
                                      "show me", "find out", "help me", "i want to know",
                                      "what is", "what are", "how much", "how many",
                                      "how does", "how do", "can you", "could you",
                                      "i need help", "please explain", "please tell",
                                      "documents", "what docs", "renewal", "requirements",
                                      "how to renew", "procedure", "process"]):
        print(f"[query_gate] ALLOW — contains query phrase: '{t}'")
        return True

    # ── 8. Longer sentences that look like natural language queries ───────
    #    If 5+ words and NOT just a list of proper nouns or numbers,
    #    it's probably a question even without a question word.
    if word_count >= 5:
        # Check if it contains answer-like or conversational confirmation phrases
        answer_phrases = [
            "is fine", "is okay", "is ok", "is good", "works for me", "fine for me",
            "doesn't matter", "anytime", "any time", "i prefer", "make it",
            "schedule it", "book for", "perfect", "i am free", "i'm free",
            "will be free", "let's do", "i will come"
        ]
        if any(ans in t_lower for ans in answer_phrases):
            # If the user is just answering the prompt conversationally, it's not a query
            # (e.g. "12pm is fine for me", "tomorrow works for me")
            print(f"[query_gate] BLOCK — contains answer phrase: '{t}'")
            return False

        # Check it's not just a sequence of names/codes
        non_alpha = sum(1 for w in words if not re.match(r'^[A-Za-z]+$', w))
        if non_alpha <= 1:  # mostly letters = natural language
            print(f"[query_gate] ALLOW — long natural language sentence: '{t}'")
            return True

    print(f"[query_gate] BLOCK — not identified as a genuine query: '{t}'")
    return False


