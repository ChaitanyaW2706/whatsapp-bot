def format_date_only(dt) -> str:
    """
    Convert a date / datetime / string value from the DB into dd-mm-yyyy.
    Returns "N/A" for None or unparseable inputs.
    """
    if not dt:
        return "N/A"

    if isinstance(dt, date):          # covers both date and datetime
        return dt.strftime("%d-%m-%Y")

    s = str(dt)
    for fmt in ("%Y-%m-%d %H:%M:%S", "%Y-%m-%d"):
        try:
            return datetime.strptime(s, fmt).strftime("%d-%m-%Y")
        except ValueError:
            continue

    return s  # safe fallback – never crash


