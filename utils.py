from datetime import datetime, date, timedelta

BOOKING_CUTOFF_HOUR = 18  # 6 PM — no same-day bookings after this hour


def get_smart_default_date() -> tuple[date, bool]:
    """
    Returns the smart default booking date based on current time.

    Rule:
      - Before 6 PM  → today is bookable → return (today, False)
      - At/After 6 PM → today is blocked  → return (tomorrow, True)

    Returns:
        (default_date, was_redirected_to_tomorrow)
        was_redirected_to_tomorrow is True when we auto-advanced past today.

    Used by all flows when customer selects "Today" after cutoff — instead of
    rejecting and re-prompting, we silently advance to tomorrow and inform them.
    """
    now = datetime.now()
    today = now.date()
    if now.hour >= BOOKING_CUTOFF_HOUR:
        return today + timedelta(days=1), True
    return today, False


def is_valid_appointment_slot(appointment_date: date, appointment_time_str: str = None) -> tuple[bool, str]:
    """
    Validate whether an appointment date+time is bookable.

    Rules:
      - If current time >= 6 PM, today is blocked entirely.
      - If current time < 6 PM, today is allowed only if the chosen time
        is at least 1 hour from now.
      - Past dates are always blocked.

    Args:
        appointment_date: The date the customer wants to book.
        appointment_time_str: Optional time string like "10:30 AM" or "14:00".

    Returns:
        (True, "") if valid.
        (False, reason_message) if invalid.
    """
    now = datetime.now()
    today = now.date()

    # ── Block past dates ────────────────────────────────────────
    if appointment_date < today:
        return False, "⚠️ That date has already passed. Please choose a future date."

    # ── Same-day logic ──────────────────────────────────────────
    if appointment_date == today:
        if now.hour >= BOOKING_CUTOFF_HOUR:
            return False, (
                f"⚠️ Same-day bookings are not accepted after 6:00 PM.\n"
                f"Please choose a date from tomorrow onwards. 📅"
            )

        # If a specific time was also provided, check it's not too soon
        if appointment_time_str:
            parsed_time = _parse_time(appointment_time_str)
            if parsed_time:
                appointment_dt = datetime.combine(today, parsed_time)
                if appointment_dt <= now + timedelta(hours=1):
                    return False, (
                        f"⚠️ Please book at least 1 hour in advance.\n"
                        f"The earliest available slot today is "
                        f"{(now + timedelta(hours=1)).strftime('%I:%M %p')}. ⏰"
                    )

    return True, ""


def _parse_time(time_str: str):
    """
    Parse a time string into a datetime.time object.
    Supports formats: '10:30 AM', '2:00 PM', '14:00', '9am', etc.
    Returns None if unparseable.
    """
    time_str = time_str.strip()
    for fmt in ("%I:%M %p", "%I:%M%p", "%H:%M", "%I %p", "%I%p"):
        try:
            return datetime.strptime(time_str.upper(), fmt.upper()).time()
        except ValueError:
            continue
    return None


def get_available_booking_dates() -> list:
    """
    Return dates available for booking based on current time.
    
    - Before 6 PM  → today + remaining week + next week
    - After 6 PM   → starts from TOMORROW (today excluded)
    """
    now = datetime.now()
    today = now.date()

    if now.hour >= BOOKING_CUTOFF_HOUR:
        # Push start to tomorrow
        start_day = today + timedelta(days=1)
    else:
        start_day = today

    # Build remaining current week from start_day
    days_until_sunday = 6 - start_day.weekday()
    end_of_week = start_day + timedelta(days=max(days_until_sunday, 0))

    dates = []
    current = start_day
    while current <= end_of_week:
        dates.append(current)
        current += timedelta(days=1)

    return dates


