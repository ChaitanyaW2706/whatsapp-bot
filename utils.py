def get_remaining_week_dates() -> list:
    """
    Return the remaining dates in the *current* week starting from the day after
    tomorrow up to and including the coming Sunday.

    Used by insurance, sales, and used-cars flows for appointment scheduling.
    """
    today = datetime.now().date()
    start_day = today + timedelta(days=2)

    days_until_sunday = 6 - start_day.weekday()   # Monday=0 … Sunday=6
    if days_until_sunday < 0:
        days_until_sunday = 0

    end_day = start_day + timedelta(days=days_until_sunday)

    dates = []
    current = start_day
    while current <= end_day:
        dates.append(current)
        current += timedelta(days=1)

    return dates


def get_next_week_dates() -> list:
    """
    Return all seven days of *next* week (Monday → Sunday).

    Used by insurance, sales, and used-cars flows for appointment scheduling.
    """
    today = datetime.now().date()
    days_until_next_monday = 7 - today.weekday()
    next_monday = today + timedelta(days=days_until_next_monday)

    return [next_monday + timedelta(days=i) for i in range(7)]


