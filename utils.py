# In-memory token store for upload tokens generated via app.py endpoints.
# Tokens generated by insurance.py are stored inside USER_STATE[phone]["upload_token"].
UPLOAD_TOKENS: dict = {}

TOKEN_EXPIRY_SECONDS = 3600  # 1 hour


def generate_upload_token(phone: str, name: str, vehicle: str) -> str:
    """
    Generate a secure UUID-based upload token and persist it in USER_STATE
    so it can be validated later by get_user_from_token().

    Stores the token under USER_STATE[phone]["upload_token"] which is the
    single source of truth checked by get_user_from_token().
    """
    token = str(uuid.uuid4())

    USER_STATE.setdefault(phone, {})
    USER_STATE[phone]["upload_token"] = {
        "token": token,
        "name": name,
        "vehicle": vehicle,
        "timestamp": time.time(),
        "mobile": phone,   # kept for convenience when retrieved via token scan
    }

    print(f"✅ Generated upload token for {phone}: {token}")
    return token


def get_user_from_token(token: str) -> dict | None:
    """
    Validate a token and return the associated user data dict, or None if the
    token is missing / expired.

    Checks USER_STATE first (tokens created by insurance flow / generate_upload_token),
    then falls back to the legacy UPLOAD_TOKENS dict kept for backward compatibility.

    Returned dict keys: mobile, name, vehicle, timestamp
    """
    # ── 1. Scan USER_STATE (primary store) ──────────────────────────────
    for phone, user_data in USER_STATE.items():
        token_data = user_data.get("upload_token")
        if not token_data:
            continue
        if token_data.get("token") != token:
            continue

        if time.time() - token_data.get("timestamp", 0) > TOKEN_EXPIRY_SECONDS:
            print(f"❌ Token expired in USER_STATE: {token}")
            user_data.pop("upload_token", None)
            return None

        print(f"✅ Valid token from USER_STATE for {phone}")
        return {
            "mobile": phone,
            "name": token_data.get("name", ""),
            "vehicle": token_data.get("vehicle", ""),
            "timestamp": token_data.get("timestamp", 0),
        }

    # ── 2. Fallback: legacy UPLOAD_TOKENS dict ───────────────────────────
    if token in UPLOAD_TOKENS:
        data = UPLOAD_TOKENS[token]
        if time.time() - data.get("timestamp", 0) > TOKEN_EXPIRY_SECONDS:
            print(f"❌ Token expired in UPLOAD_TOKENS: {token}")
            UPLOAD_TOKENS.pop(token, None)
            return None

        print(f"✅ Valid token from UPLOAD_TOKENS for {data['mobile']}")
        return data

    print(f"❌ Token not found: {token}")
    return None


def revoke_upload_token(token: str) -> None:
    """Remove a token from both USER_STATE and UPLOAD_TOKENS after successful upload."""
    # Remove from legacy dict
    UPLOAD_TOKENS.pop(token, None)

    # Remove from USER_STATE
    for phone, user_data in USER_STATE.items():
        token_data = user_data.get("upload_token")
        if token_data and token_data.get("token") == token:
            user_data.pop("upload_token", None)
            print(f"✅ Token revoked from USER_STATE for {phone}")
            break


