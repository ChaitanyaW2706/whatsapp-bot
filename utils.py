# INTELLIGENT FLOW-AWARE QUERY COMPREHENSION
# Understands customer queries BEFORE rigidly following flow
# ============================================================
def understand_customer_in_flow_context(phone: str, user_text: str, current_state: str, 
                                        flow_context: dict = None) -> dict:
    """
    Use LLM to intelligently understand what customer is really asking/doing,
    even within a predefined flow state.
    
    This MUST be called FIRST in flow handlers to determine if:
    1. Customer is answering the flow question normally → return action="CONTINUE_FLOW"
    2. Customer is asking a question → return action="ANSWER_QUERY" + answer
    3. Customer wants to switch flows/go back → return action="SWITCH_CONTEXT" + action
    4. Customer needs clarification → return action="CLARIFY"
    5. Customer input is ambiguous → return action="AMBIGUOUS"
    
    Returns dict with keys:
    - action: one of ["CONTINUE_FLOW", "ANSWER_QUERY", "SWITCH_CONTEXT", "CLARIFY", "AMBIGUOUS"]
    - should_proceed_with_flow: bool (True if flow should continue normally)
    - response: str (AI's response if action is not CONTINUE_FLOW)
    - extracted_data: dict (normalized/extracted data if extractable)
    - flow_action: str (specific action like "BACK", "MAIN_MENU", "CHANGE_CRITERIA", etc.)
    """
    try:
        if not user_text or not user_text.strip():
            return {
                "action": "AMBIGUOUS",
                "should_proceed_with_flow": False,
                "response": "I didn't catch that. Could you please say that again?",
                "extracted_data": {},
                "flow_action": None
            }
        
        flow_context = flow_context or {}
        
        system_prompt = f"""You are an intelligent flow assistant for an automotive chatbot.
Your job is to understand what the customer REALLY wants, even when they're in the middle of a form/flow.

CURRENT FLOW STATE: {current_state}
FLOW CONTEXT: {json.dumps(flow_context, indent=2)}

Analyze the customer's message and determine:
1. Are they answering the current question normally? (e.g., flow expects "Brand" and they say "Hyundai")
2. Are they asking a question about the product/service?
3. Do they want to go back/switch flows/cancel?
4. Are they expressing concerns or needs that aren't aligned with the current step?
5. Is it ambiguous what they want?

Return ONLY valid JSON with this structure:
{{
    "action": "<CONTINUE_FLOW|ANSWER_QUERY|SWITCH_CONTEXT|CLARIFY|AMBIGUOUS>",
    "confidence": <0.0-1.0>,
    "should_proceed_with_flow": <true|false>,
    "response": "<Your intelligent response to customer>",
    "extracted_data": {{"key": "value"}},
    "flow_action": "<specific action like BACK, MAIN_MENU, CHANGE_CRITERIA, TALK_TO_ADVISOR, or null>"
}}

Guidelines:
- If they clearly answer the question, use CONTINUE_FLOW
- If they ask "what is...", "how does...", "tell me about...", use ANSWER_QUERY
- If they say "go back", "main menu", "talk to someone", use SWITCH_CONTEXT with flow_action
- If they express doubt/concern, use CLARIFY or ANSWER_QUERY
- If unclear, use AMBIGUOUS
- Always be helpful and conversational
- Acknowledge their concern before continuing the flow
"""

        user_prompt = f'Customer message: "{user_text}"'

        response = _client.chat.completions.create(
            model=_MODEL_NAME,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt}
            ],
            temperature=0.3,
            max_tokens=500,
            response_format={"type": "json_object"}
        )
        
        result = json.loads(response.choices[0].message.content.strip())
        
        print(f"🧠 [Flow Context Understanding] State={current_state}")
        print(f"   Action: {result.get('action')}")
        print(f"   Confidence: {result.get('confidence')}")
        print(f"   Flow Action: {result.get('flow_action')}")
        
        return result
        
    except Exception as e:
        print(f"❌ Flow context understanding error: {e}")
        # Fallback: assume they're trying to continue the flow
        return {
            "action": "CONTINUE_FLOW",
            "should_proceed_with_flow": True,
            "response": None,
            "extracted_data": {},
            "flow_action": None,
            "confidence": 0.5
        }