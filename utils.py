# SMART DATE RESOLVER
# Maps natural language dates to IDs (e.g. '24th' -> 'DATE_20240424')
# ============================================================

def resolve_date_from_text(text: str, available_dates: list, prefix: str) -> str | None:
    """
    Intelligently match user's free-text date input to one of the available_dates.
    Uses multiple strategies: ordinal matching, month matching, weekday matching, and AI fallback.
    Handles dates like "4th may", "5 may", "may 5", "24", "Monday", "tomorrow", etc.
    """
    if not text or not available_dates:
        return None
    
    t = text.lower().strip()
    print(f"📅 [utils] resolve_date_from_text: '{text}' (normalized: '{t}')")
    
    # ── Remove ordinal suffixes (st, nd, rd, th) ──────────────────────────
    t_clean = re.sub(r'(st|nd|rd|th)\b', '', t)
    print(f"   Cleaned ordinals: '{t_clean}'")
    
    # Extract all numbers and find the first valid day number
    nums = re.findall(r'\d+', t_clean)
    day_num = None
    if nums:
        day_num = int(nums[0])
        if 1 <= day_num <= 31:
            print(f"   Extracted day number: {day_num}")
        else:
            day_num = None
    
    # ── Month name mapping (full and abbreviated) ─────────────────────────
    month_map = {
        # Full month names
        "january": 1, "february": 2, "march": 3, "april": 4, "may": 5, "june": 6,
        "july": 7, "august": 8, "september": 9, "october": 10, "november": 11, "december": 12,
        # Abbreviated month names
        "jan": 1, "feb": 2, "mar": 3, "apr": 4, "jun": 6,
        "jul": 7, "aug": 8, "sep": 9, "sept": 9, "oct": 10, "nov": 11, "dec": 12,
        # Hindi months
        "january": 1, "phbruary": 2, "march": 3, "april": 4, "may": 5,
    }
    
    month_num = None
    for mname, mnum in month_map.items():
        if mname in t_clean:
            month_num = mnum
            print(f"   Extracted month: {mname} → {mnum}")
            break
    
    # ── Strategy 1: Day + Month (e.g. "4th may", "5 april") ──────────────
    if day_num and month_num:
        print(f"   Trying day+month match: day={day_num}, month={month_num}")
        for d in available_dates:
            if d.day == day_num and d.month == month_num:
                result = f"{prefix}{d.strftime('%Y%m%d')}"
                print(f"   ✅ Day+month match found: {d.strftime('%Y-%m-%d')} → {result}")
                return result
        print(f"   ❌ No date found for {day_num}/{month_num}")
    
    # ── Strategy 2: Day only (e.g. "24", "5") - if text is very short ───
    if day_num and len(t) <= 5:
        print(f"   Trying day-only match: day={day_num}")
        for d in available_dates:
            if d.day == day_num:
                result = f"{prefix}{d.strftime('%Y%m%d')}"
                print(f"   ✅ Day-only match found: {d.strftime('%Y-%m-%d')} → {result}")
                return result
    
    # ── Strategy 3: Weekday name (e.g. "Monday", "Wed", "Friday") ────────
    weekday_map = {
        "monday": 0, "mon": 0, "tuesday": 1, "tue": 1, "wednesday": 2, "wed": 2,
        "thursday": 3, "thu": 3, "friday": 4, "fri": 4, "saturday": 5, "sat": 5,
        "sunday": 6, "sun": 6
    }
    for wday_name, wday_idx in weekday_map.items():
        if wday_name in t_clean:
            print(f"   Trying weekday match: {wday_name} (index={wday_idx})")
            for d in available_dates:
                if d.weekday() == wday_idx:
                    result = f"{prefix}{d.strftime('%Y%m%d')}"
                    print(f"   ✅ Weekday match found: {d.strftime('%Y-%m-%d')} → {result}")
                    return result
            break
    
    # ── Strategy 4: Relative dates ──────────────────────────────────────
    today = date.today()
    if "today" in t_clean or "this day" in t_clean:
        for d in available_dates:
            if d == today:
                result = f"{prefix}{d.strftime('%Y%m%d')}"
                print(f"   ✅ 'Today' match: {d.strftime('%Y-%m-%d')} → {result}")
                return result
    
    if "tomorrow" in t_clean or "next day" in t_clean:
        tomorrow = today + timedelta(days=1)
        for d in available_dates:
            if d == tomorrow:
                result = f"{prefix}{d.strftime('%Y%m%d')}"
                print(f"   ✅ 'Tomorrow' match: {d.strftime('%Y-%m-%d')} → {result}")
                return result
    
    # ── Strategy 5: Groq AI fallback for complex/multilingual inputs ─────
    if len(available_dates) > 0:
        try:
            dates_list = "\n".join(
                [f"- {d.strftime('%A')}, {d.strftime('%d %B %Y')} (ID: {prefix}{d.strftime('%Y%m%d')})"
                 for d in sorted(available_dates)]
            )
            
            prompt = f"""Match the user's date input to ONE exact date from the list below.
The date might be in English, Hindi, Tamil, Telugu, or any language.
Extract the specific date they mentioned and find the closest match.

AVAILABLE DATES:
{dates_list}

USER MESSAGE: "{text}"

RULES:
1. If they mention a specific day/month/date, find EXACTLY that date
2. If ambiguous (e.g., "24"), choose the next occurrence of day 24
3. If they say "today", "tomorrow", "Monday", match to that day
4. Return ONLY valid JSON: {{"matched_id": "<full ID from list>", "reason": "<short reason>"}}
5. If NO match possible, return {{"matched_id": "NONE", "reason": "no match found"}}"""

            response = _client.chat.completions.create(
                model=_MODEL_NAME,
                messages=[{"role": "user", "content": prompt}],
                temperature=0.0,
                max_tokens=100,
                response_format={"type": "json_object"}
            )
            
            result = json.loads(response.choices[0].message.content.strip())
            matched_id = result.get("matched_id", "NONE")
            reason = result.get("reason", "")
            
            print(f"🤖 [utils] AI fallback: '{text}' → '{matched_id}' (reason: {reason})")
            
            if matched_id and matched_id != "NONE" and matched_id.startswith(prefix):
                return matched_id
            
        except Exception as e:
            print(f"❌ [utils] AI fallback error: {e}")
    
    print(f"   ❌ No match found for: '{text}'")
    return None


